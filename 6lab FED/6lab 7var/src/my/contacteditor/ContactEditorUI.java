private void startClientReceiver() {
    try {
        while (true) {
            byte[] buffer = new byte[1024];
            DatagramPacket packet = new DatagramPacket(buffer, buffer.length);

            sendSocket.receive(packet); // слушаем ответ

            String msg = new String(packet.getData(), 0, packet.getLength());
            String[] parts = msg.split(";");

            if (parts.length < 2) continue;

            int row = Integer.parseInt(parts[0]);
            double result = Double.parseDouble(parts[1]);

            javax.swing.SwingUtilities.invokeLater(() -> {
                jTable1.setValueAt(result, row, 3);
            });
        }
    } catch (Exception e) {
        e.printStackTrace();
    }
}
private double calculateIntegral(double a, double b, double h) {
    double sum = 0.0;

    for (double x = a; x < b; x += h) {
        double x2 = x + h;

        if (x <= 0 || x2 <= 0) continue;

        double y1 = 1 / Math.log(x);
        double y2 = 1 / Math.log(x2);

        sum += (y1 + y2) / 2 * h;
    }

    return sum;
}
    private void startServer() {
    try {
        while (true) {
            byte[] buffer = new byte[1024];
            DatagramPacket packet = new DatagramPacket(buffer, buffer.length);

            receiveSocket.receive(packet); 

            String msg = new String(packet.getData(), 0, packet.getLength());
            String[] parts = msg.split(";");

            if (parts.length < 4) continue;

            int row = Integer.parseInt(parts[0]);
            double lower = Double.parseDouble(parts[1]);
            double upper = Double.parseDouble(parts[2]);
            double step = Double.parseDouble(parts[3]);

            double result = calculateIntegral(lower, upper, step);

            // отправка ответа обратно
            String response = row + ";" + result;
            byte[] data = response.getBytes();

            DatagramPacket reply = new DatagramPacket(
                    data,
                    data.length,
                    packet.getAddress(),
                    packet.getPort()
            );

            receiveSocket.send(reply);
        }

    } catch (Exception e) {
        e.printStackTrace();
    }
}
